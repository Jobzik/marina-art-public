package com.app.model;

public class Size {
    private long id;
    private int imgWidth;
    private int imgHeight;
    private int paperWidth;
    private int paperHeight;
    private int glassWidth;
    private int glassHeight;
    private int frameWidth;
    private int frameHeight;
    private int mountWidth;
    private int mountHeight;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getImgWidth() {
        return imgWidth;
    }

    public void setImgWidth(int imgWidth) {
        this.imgWidth = imgWidth;
    }

    public int getImgHeight() {
        return imgHeight;
    }

    public void setImgHeight(int imgHeight) {
        this.imgHeight = imgHeight;
    }

    public int getPaperWidth() {
        return paperWidth;
    }

    public void setPaperWidth(int paperWidth) {
        this.paperWidth = paperWidth;
    }

    public int getPaperHeight() {
        return paperHeight;
    }

    public void setPaperHeight(int paperHeight) {
        this.paperHeight = paperHeight;
    }

    public int getGlassWidth() {
        return glassWidth;
    }

    public void setGlassWidth(int glassWidth) {
        this.glassWidth = glassWidth;
    }

    public int getGlassHeight() {
        return glassHeight;
    }

    public void setGlassHeight(int glassHeight) {
        this.glassHeight = glassHeight;
    }

    public int getFrameWidth() {
        return frameWidth;
    }

    public void setFrameWidth(int frameWidth) {
        this.frameWidth = frameWidth;
    }

    public int getFrameHeight() {
        return frameHeight;
    }

    public void setFrameHeight(int frameHeight) {
        this.frameHeight = frameHeight;
    }

    public int getMountWidth() {
        return mountWidth;
    }

    public void setMountWidth(int mountWidth) {
        this.mountWidth = mountWidth;
    }

    public int getMountHeight() {
        return mountHeight;
    }

    public void setMountHeight(int mountHeight) {
        this.mountHeight = mountHeight;
    }
}
